use super::Locale;

#[derive(Debug, Clone)]
pub struct HexagramInfo {
    pub index: u8,
    pub hanja: &'static str,
    pub name: &'static str,
    pub name_en: &'static str,
    pub name_zh: &'static str,
    pub name_ru: &'static str,
    pub desc_ko: &'static str,
    pub desc_en: &'static str,
    pub desc_zh: &'static str,
    pub desc_ru: &'static str,
}

pub fn get_hexagram_info(index: u8) -> HexagramInfo {
    let list = get_all_hexagrams();
    if (1..=64).contains(&index) {
        list[(index - 1) as usize].clone()
    } else {
        list[0].clone()
    }
}

pub fn get_yao_name(line_idx: u8, is_yang: bool, locale: Locale) -> String {
    match locale {
        Locale::Ko => {
            let prefix = if is_yang { "구" } else { "육" };
            match line_idx {
                1 => format!("초{}", prefix),
                2 => format!("{}이", prefix),
                3 => format!("{}삼", prefix),
                4 => format!("{}사", prefix),
                5 => format!("{}오", prefix),
                _ => format!("상{}", prefix),
            }
        }
        Locale::En => {
            let num = if is_yang { "Nine" } else { "Six" };
            match line_idx {
                1 => format!("First {}", num),
                2 => format!("Second {}", num),
                3 => format!("Third {}", num),
                4 => format!("Fourth {}", num),
                5 => format!("Fifth {}", num),
                _ => format!("Top {}", num),
            }
        }
        Locale::Zh => {
            let prefix = if is_yang { "九" } else { "六" };
            match line_idx {
                1 => format!("初{}", prefix),
                2 => format!("{}二", prefix),
                3 => format!("{}三", prefix),
                4 => format!("{}四", prefix),
                5 => format!("{}五", prefix),
                _ => format!("上{}", prefix),
            }
        }
        Locale::Ru => {
            let num = if is_yang {
                "Девятка"
            } else {
                "Шестерка"
            };
            match line_idx {
                1 => format!("Начальная {}", num),
                2 => format!("Вторая {}", num),
                3 => format!("Третья {}", num),
                4 => format!("Четвертая {}", num),
                5 => format!("Пятая {}", num),
                _ => format!("Верхняя {}", num),
            }
        }
    }
}

